/*
 * 题目：杨辉三角
 * 来源：牛客网（noob51）
 * 知识类型：C++基础语法 | 二维数组/vector操作 | 循环结构 | 递推公式 | 格式输出
 * 题目原文：
 *   描述：杨辉三角形第i+1行是二项式展开(a+b)ⁱ的系数，任意元素等于上一行同列+上一行前一列元素之和。给定正整数n，输出前n行。
 *   输入描述：
 *     一行输入整数n（1≦n≦34）。
 *   输出描述：
 *     输出前n行杨辉三角，每行元素依次输出，两数之间用一个空格分隔，行末无多余空格。
 *   示例1：
 *     输入：4
 *     输出：
 *     1
 *     1 1
 *     1 2 1
 *     1 3 3 1
 *   示例2：
 *     输入：1
 *     输出：1
 *   难度：入门，通过率52.84%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 准确实现杨辉三角递推公式：yh[i][j] = yh[i-1][j-1] + yh[i-1][j]；
 *   2. 显式处理边界（每行首尾元素为1），避免依赖数组隐式初始化；
 *   3. 严格控制输出格式，行末无多余空格；
 *   4. 适配n≤34的范围，保证数据无溢出、无越界。
 * 解题思路：
 *   1. 用vector构建二维数组（灵活且避免全局数组的隐式初始化依赖）；
 *   2. 遍历每行，先初始化首尾元素为1，再递推填充中间元素；
 *   3. 按行输出，通过j < i判断是否输出空格，保证行末无多余空格；
 *   4. 每行输出完成后换行，匹配题目格式要求。
 */
#include <bits/stdc++.h>
using namespace std;

int main()
{
    int n;
    cin >> n;
    // 用vector构建n+1行n+1列的二维数组，初始值全0，更灵活且可控
    vector<vector<int>> yh(n + 1, vector<int>(n + 1, 0));

    // 核心逻辑：显式处理边界 + 递推中间值
    for (int i = 1; i <= n; i++)
    {
        yh[i][1] = 1; // 每行第一个元素固定为1
        yh[i][i] = 1; // 每行最后一个元素固定为1
        // 仅递推填充中间元素（j从2到i-1）
        for (int j = 2; j < i; j++)
        {
            yh[i][j] = yh[i - 1][j - 1] + yh[i - 1][j];
        }

        // 输出当前行，控制行末无多余空格
        for (int j = 1; j <= i; j++)
        {
            cout << yh[i][j];
            if (j < i)
                cout << " "; // 非最后一个元素才输出空格
        }
        cout << endl;
    }
    return 0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 边界处理：显式将每行首尾元素设为1，避免依赖全局数组的0初始化，逻辑更清晰；
2. 递推范围：仅对j=2到i-1的中间元素递推，减少无效计算；
3. 输出格式：通过j < i判断是否输出空格，彻底避免行末多余空格；
4. 容器选择：vector比静态数组更灵活，无全局数组的初始化风险，符合工程最佳实践。

【易错点】
1. 原始代码问题：依赖全局数组a[0][0]=1的初始化，i=2时a[1][2]为0，会导致计算错误（如n=3时第二行输出"1 0"）；
2. 输出格式错误：行末输出多余空格，不符合题目要求；
3. 递推逻辑错误：j的循环范围包含首尾元素，重复计算导致值错误；
4. 数组越界：静态数组维度不足，或索引处理不当导致越界。

【思维扩展】
1. 空间优化解法（一维vector滚动更新，空间O(n)）：
   int main() {
       int n;
       cin >> n;
       vector<int> dp(n + 1, 0);
       dp[1] = 1;
       cout << 1 << endl;

       for (int i = 2; i <= n; i++) {
           // 从后往前更新，避免覆盖未使用的前一轮值
           for (int j = i; j >= 2; j--) {
               dp[j] = dp[j] + dp[j - 1];
           }
           dp[i] = 1; // 补全当前行最后一个元素

           // 输出当前行
           for (int j = 1; j <= i; j++) {
               cout << dp[j];
               if (j < i) cout << " ";
           }
           cout << endl;
       }
       return 0;
   }
   优势：空间复杂度从O(n²)降为O(n)，避免二维数组的内存占用，适合n更大的场景。
*/

