/*
 * 题目：计算带余除法
 * 来源：牛客网（noob8）
 * 题目原文：
 *   给定两个整数 a 和 b（1≦a,b≦10⁴），请计算 a 除以 b 的整数商和余数。
 *   输入：一行两个整数 a,b（1≦a,b≦10⁴），被除数/除数；
 *   输出：两个整数（商 余数），中间空格隔开。
 *   示例：输入15 2 → 输出7 1。
 * 核心要求：
 *   1. 计算整数商和余数；
 *   2. 输出格式为「商 余数」（单个空格分隔）。
 * 关键思路：
 *   1. 商 = a/b，余数 = a%b；
 *   2. 输出用' '分隔结果。
 */
#include <iostream>
using namespace std;

int main() {
    int a,b;
    cin>>a>>b;
    cout<<a/b<<' '<<a%b<<endl;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. a/b 是整数商（自动舍小数），a%b 直接得余数；
2. 输出必须用单个空格分隔商和余数。

【易错点】
1. 输出顺序错（余数在前、商在后）；
2. 输出无空格/多空格（格式判错）；
3. 误判数据类型（本题int足够，无需long long）。
*/