/*
 * 题目：疫情死亡率
 * 来源：牛客网（noob7）
 * 题目原文：
 *   描述：给定确诊人数c和死亡人数d（1≦d≦c≦10⁷），计算死亡率（d/c×100%），以百分数形式输出。
 *   输入描述：一行两个整数c,d（1≦d≦c≦10⁷），确诊人数/死亡人数。
 *   输出描述：输出死亡率（末尾加%），误差≤10⁻³即被接受。
 *   示例1：输入10433 280 → 输出2.684%；示例2：输入929100 922311 → 输出99.269%。
 *   难度：入门，通过率40.61%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 计算死亡率：(d/c)×100，保留3位小数；
 *   2. 输出格式为「数值%」（末尾加百分号）；
 *   3. 误差≤10⁻³即可，无需严格匹配小数位数（但保留3位最稳妥）。
 * 解题思路：
 *   1. 用int接收c、d（10⁷在int范围内）；
 *   2. 强制类型转换为double计算(d/c)（避免整数除法）；
 *   3. 乘以100后用fixed+setprecision(3)保留3位小数，末尾拼接%输出。
 */
#include <bits/stdc++.h>
#include <csetjmp>
#include <iomanip>
using namespace std;
int main (){
    int c,d;
    cin>>c>>d;
    
    cout<<fixed<<setprecision(3)<<(double)d/c*100<<"%"<<endl;
    //切记，百分数100%，要再乘以100答案才正确
    return 0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 类型转换：必须将d/c转为double计算（否则整数除法会得到0）；
2. 死亡率计算：(d/c)×100（核心公式，漏乘100会导致结果缩小100倍）；
3. 输出格式：fixed+setprecision(3)保留3位小数，末尾加%。

【易错点】
1. 整数除法陷阱：直接d/c（如280/10433=0，而非0.02684）；
2. 漏乘100：仅输出d/c的结果（如0.02684→2.684%，漏乘则输出0.02684%）；
3. 格式错误：漏加%或小数位数不符（保留3位最贴合示例，也满足误差要求）；
4. 类型误用：用float计算（精度不足，可能触发误差超限）。
*/