/*
 * 题目：整数的十位
 * 来源：牛客网（noob10）
 * 题目原文：
 *   描述：给定一个整数a（0≦a≦10⁹），计算其十位数字；十位不存在则输出0。
 *   输入描述：在一行中输入一个整数a（0≦a≦10⁹）。
 *   输出描述：输出a的十位数字（无则输出0）。
 *   示例1：输入114 → 输出1；示例2：输入6 → 输出0。
 *   难度：入门，通过率67.41%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 提取0~10⁹范围内整数a的十位数字；
 *   2. 不足两位的整数（如6），十位输出0。
 * 解题思路：
 *   1. 用int接收a（10⁹在int范围内）；
 *   2. 先整除10（a/10）去掉个位，再对10取余（%10）得到十位；
 *   3. 直接输出计算结果（不足两位时a/10=0，0%10=0，天然符合要求）。
 */
#include <stdio.h>

int main()
{
    int a;
    scanf("%d", &a);
    printf("%d", a / 10 % 10);
    return 0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 核心公式：a/10%10（先整除10去个位，再取余10得十位）；
2. 边界处理：不足两位的数（如6），a/10=0，0%10=0，无需额外判断；
3. 数据类型：int适配0~10⁹范围，无需long long。

【易错点】
1. 公式错误：误用a%10/10（先取余得个位，再整除无意义）；
2. 多余判断：手动if判断a<10则输出0（公式已天然处理，无需额外代码）；
3. 输入输出：用cin/cout时漏写格式（本题用C语言scanf/printf更简洁）。
*/