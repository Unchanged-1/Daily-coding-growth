/*
 * 题目：整数的个位
 * 来源：牛客网（noob9）
 * 题目原文：
 *   描述：给定一个整数a（0≤a≤10⁴），求该整数的个位数字（绝对值对10取余的结果）。
 *   输入描述：在一行中输入一个整数a（0≤a≤10⁴）。
 *   输出描述：输出一个整数，表示a的个位数字。
 *   示例1：输入114 → 输出4。
 *   难度：入门，通过率73.87%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 计算0~10⁴范围内整数a的个位数字；
 *   2. 核心逻辑为a对10取余。
 * 解题思路：
 *   1. 用int类型接收输入的整数a；
 *   2. 利用取余运算a%10直接得到个位数字；
 *   3. 输出取余结果即可。
 */
#include <iostream>
using namespace std;

int main() {
    int a;
    cin>>a;
    cout<<a%10<<endl;
    return 0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 取余特性：a%10 可直接获取整数的个位数字（0≤a≤10⁴范围内无需处理负数）；
2. 数据类型：int完全适配0~10⁴的数值范围，无需额外类型转换。

【易错点】
1. 取余数错误：误用a%1（结果恒为0）或a%100（获取后两位），而非a%10；
2. 多余处理：对非负整数a额外做绝对值运算（题目a≥0，无需abs(a)）；
3. 输出格式：额外添加空格/换行（仅需输出个位数字本身）。
*/