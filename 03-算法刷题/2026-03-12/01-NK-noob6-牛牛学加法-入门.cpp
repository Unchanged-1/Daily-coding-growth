/*
 * 题目：【模板】牛牛学加法
 * 来源：牛客网（noob6）
 * 题目原文：
 *   描述：给定两个整数a和b（0≦a,b≦1000），计算它们的和并输出结果。
 *   输入描述：在一行中输入两个整数a,b（0≦a,b≦1000）。
 *   输出描述：输出一个整数，表示a+b的值。
 *   示例1：输入1 2 → 输出3；示例2：输入1000 0 → 输出1000。
 *   难度：入门，通过率65.01%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 读取0~1000范围内的两个整数a、b；
 *   2. 计算a+b的和，直接输出结果。
 * 解题思路：
 *   1. 用int类型接收a、b（范围0~1000，int完全适配）；
 *   2. 计算a+b的和，用cout输出结果。
 */
#include <iostream>
using namespace std;

int main() {
    int a, b;
    cin >> a >> b ;
    cout << a + b << endl;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 数据类型：int可覆盖0~1000的数值范围，无需更大类型；
2. 计算逻辑：直接a+b求和，无复杂边界处理；
3. 输入输出：cin读取两个整数，cout输出和即可。

【易错点】
1. 输入格式错误：试图用多行读取a、b（题目要求一行输入）；
2. 输出冗余：额外输出"a+b="等字符（仅需输出求和结果）；
3. 类型误用：不必要地使用long long（徒增代码复杂度）。
*/