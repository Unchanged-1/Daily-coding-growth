/*
 * 题目：牛牛学说话之-整数
 * 来源：牛客网（noob2）
 * 题目原文：
 *   描述：牛牛刚刚出生，嗷嗷待哺，一开始他只能学说简单的数字，你跟他说一个整数，他立刻就能学会。输入一个整数n，输出这个整数。
 *   输入描述：在一行上一个整数 n (−10⁴ ≤n≤10⁴)。
 *   输出描述：输出整数 n。
 *   难度：入门，通过率52.08%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 读取范围在-10⁴~10⁴的整数n；
 *   2. 直接输出该整数，无格式额外要求。
 * 解题思路：
 *   1. 用int类型接收输入的整数n；
 *   2. 直接输出n即可。
 */
#include <bits/stdc++.h>
using namespace std;
int main(){
    int n;
    cin>>n;
    cout<<n<<endl;
}

