/*
 * 题目：多组输入a+b II
 * 来源：牛客网（noob29）
 * 知识类型：C++基础语法 | 循环结构（for循环） | 多组输入处理 | 算术运算（加法） | 输入输出格式控制
 * 题目原文：
 *   描述：给定多组数据，每组两个整数a,b（0≦a,b≦100），计算每组的和。
 *   输入描述：
 *     第一行输入整数T（1≦T≦100），表示数据组数；
 *     接下来T行，每行输入两个整数a,b，空格隔开。
 *   输出描述：每组数据输出一行，为a+b的结果。
 *   示例1：输入2、1 1、2 2 → 输出2、4。
 *   难度：入门，通过率66.09%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 正确处理多组输入（先读组数T，再循环T次读a,b）；
 *   2. 每组数据输出一行求和结果。
 * 解题思路：
 *   1. 用int接收组数T；
 *   2. 用for循环执行T次：每次读a和b，计算并输出a+b；
 *   3. 循环变量i从0或1开始均可，只要循环次数为T次。
 */
#include<bits/stdc++.h>
using namespace std;
int main() {
    int t,a,b;
    cin>>t;  // 读取数据组数T
    // 循环T次处理每组数据：i从0开始，循环t次（0~t-1）
    for(int i=0;i<t;i++){
        cin>>a>>b;  // 读取每组的两个整数
        cout<<a+b<<endl;  // 输出每组的和，每行一个结果
    }
    return 0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 多组输入逻辑：必须先读组数T，再循环T次读取每组数据（顺序不可颠倒）；
2. 循环次数：for循环i从0或1开始均可（i=0时循环条件i<t，i=1时i<=t，均执行t次）；
3. 数据范围：a,b≤100，和≤200，int完全覆盖，无需其他类型；
4. 输出格式：每组结果占一行（用endl换行，符合题目要求）。

【易错点】
1. 输入顺序错误：先读a,b再读T（导致T读取的是第一组的a，数据全部错位）；
2. 循环次数错误：
   - 写成i<=t（i从0开始时，循环t+1次，超出组数，会多读/多输出）；
   - 写成i<t-1（循环t-1次，少处理一组数据）；
3. 输出格式错误：将多组结果输出在同一行（如cout<<a+b<<' '，未换行）；
4. 变量命名错误：把t写成T（大小写敏感，cin>>t和cin>>T是两个不同变量）；
5. 语法错误：for循环后漏写{}（单语句无影响，但多语句时会出错，建议养成加{}的习惯）。
*/