/*
 * 题目：比大小
 * 来源：牛客网（noob23）
 * 题目原文：
 *   描述：比较整数a和b的大小，输出<、=、>其中一个。
 *   输入描述：一行输入两个整数a,b（1≦a,b≦10⁴），空格隔开。
 *   输出描述：输出比较结果（<、=、>，无引号）。
 *   示例1：输入1 2 → 输出<；示例2：输入1 1 → 输出=；示例3：输入2 1 → 输出>。
 *   难度：入门，通过率64.77%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 正确判断a和b的大小关系；
 *   2. 输出固定字符<、=、>（无多余字符/空格）。
 * 解题思路：
 *   1. 用int接收a、b（10⁴范围，int足够）；
 *   2. 多条件判断：先判断a>b，再判断a==b，最后a<b；
 *   3. 按条件输出对应字符。
 */
#include <bits/stdc++.h>
using namespace std;
int main (){
    int a,b;
    cin>>a>>b;
    if(a>b){          // a大于b，输出>
        cout<<'>'<<endl;
    }
    else if(a==b){    // a等于b，输出=
        cout<<'='<<endl;
    }
    else{             // 剩余情况：a小于b，输出<
        cout<<'<'<<endl;
    }
    return 0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 条件顺序：先判断>，再判断=，最后默认<（顺序不影响结果，但逻辑更清晰）；
2. 输出字符：用单引号（字符）或双引号（字符串）均可（如cout<<">"也正确）；
3. 输入格式：题目保证一行输入两个整数，cin自动处理空格/换行，无需额外处理。

【易错点】
1. 运算符错误：
   - 把==写成=（赋值运算符，条件恒为真，结果错误）；
   - 把>写成<（判断逻辑颠倒）；
2. 输出错误：
   - 输出带引号（如"<"，题目要求无引号）；
   - 输出多余空格/换行（如cout<<" < "<<endl）；
3. 语法错误：if/else if后漏写{}（单语句无影响，但建议养成习惯）。
*/