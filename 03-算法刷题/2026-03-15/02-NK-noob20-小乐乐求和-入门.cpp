/*
 * 题目：小乐乐求和
 * 来源：牛客网（noob20）
 * 题目原文：
 *   描述：计算从1到n的自然数之和（1≦n≦10⁹）。
 *   输入描述：一行输入正整数n（1≦n≦10⁹）。
 *   输出描述：输出1~n的自然数之和（整数）。
 *   示例1：输入1 → 输出1；示例2：输入10 → 输出55。
 *   提示：高斯公式 ∑(1~n) = n(n+1)/2。
 *   难度：入门，通过率26.76%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 计算1~n的和，需适配n=10⁹的大范围（避免超时/溢出）；
 *   2. 输出整数结果。
 * 解题思路（优化版，高斯公式，避免超时）：
 *   1. 用long long接收n（n=10⁹时，n*(n+1)≈10¹⁸，远超int范围）；
 *   2. 代入高斯公式：sum = n*(n+1)/2（先乘后除，保证整数结果）；
 *   3. 输出sum。
 */
#include <bits/stdc++.h>
using namespace std;
int main(){
    long long n;  // 必须用long long：n=1e9时，n*(n+1)=1e18，int存不下
    cin>>n;
    // 高斯公式：1~n求和 = n*(n+1)/2（替代循环，避免超时）
    long long sum = n * (n + 1) / 2;  
    cout<<sum<<endl;
    return 0;
}

