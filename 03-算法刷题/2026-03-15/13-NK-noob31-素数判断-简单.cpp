/*
 * 题目：素数判断
 * 来源：牛客网（noob31）
 * 知识类型：C++基础语法 | 素数判定（优化版） | 循环结构（双层for循环） | 条件判断 | 数学优化（开平方/奇偶筛选） | 布尔类型使用
 * 题目原文：
 *   描述：给定T个正整数n（1≦n≦10⁵），判断每个n是否为素数（素数：>1且仅能被1和自身整除）。
 *   输入描述：
 *     第一行输入整数T（1≦T≦10），表示测试用例数；
 *     接下来T行，每行输入一个正整数n。
 *   输出描述：每行输出Yes/No（首字母大写），素数输出Yes，否则No。
 *   示例1：输入2、1、2 → 输出No、Yes。
 *   难度：简单，通过率27.90%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 正确判定素数（优化算法，避免超时）；
 *   2. 处理边界值（n=1、n=2、偶数）；
 *   3. 按格式输出Yes/No（首字母大写）。
 * 解题思路（优化版素数判定）：
 *   1. 读入测试用例数T，循环T次处理每个n；
 *   2. 边界处理：n=2→素数；n=1或偶且≠2→非素数；
 *   3. 奇数判定：只需遍历3~√n的奇数，若能整除则非素数，否则是素数；
 *   4. 按结果输出Yes/No。
 */
#include<bits/stdc++.h>
using namespace std;
int main() {
    int T,n;
    cin>>T;
    for(int i=0;i<T;i++){  // 处理T组测试用例
        cin>>n;
        
        // 边界1：n=2是唯一的偶素数
        if(n==2){
             cout<<"Yes"<<endl;
             continue;
        }
        
        // 边界2：n=1 或 偶且≠2 → 非素数
        if((n==1)||(n%2==0&&n!=2)){
             cout<<"No"<<endl;
             continue;
        }
        
        // 处理奇数n（≥3）：优化判定逻辑
        else{
            bool is_prime = true;  // 标记是否为素数
            int sqrt_n = sqrt(n);  // 只需判断到√n（数学优化，减少循环次数）
            // 只遍历奇数（偶数已提前排除），步长+2
            for(int j=3;j<=sqrt_n;j+=2){
                if(n%j==0){  // 能被整除，非素数
                    is_prime = false;
                    break;    // 提前终止循环，优化效率
                }
            }
            // 输出结果
            if(is_prime) cout<<"Yes"<<endl;
            else cout<<"No"<<endl;
        }
    }
    return 0;
}

