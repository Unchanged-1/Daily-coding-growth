/*
 * 题目：牛牛是否被叫家长
 * 来源：牛客网（noob26）
 * 知识类型：C++基础语法 | 算术运算（平均分计算） | 条件判断（if-else） | 浮点运算 | 输出格式控制
 * 题目原文：
 *   描述：三科平均分Avg=(A+B+C)/3，Avg<60则被叫家长（输出Y），否则不被叫（输出N）。
 *   输入描述：一行输入三个整数A,B,C（0≦A,B,C≦100），空格隔开（数学、语文、英语成绩）。
 *   输出描述：被叫家长输出YES，否则输出NO（示例验证为大写）。
 *   示例1：输入80 60 50 → 输出NO；示例2：输入70 55 40 → 输出YES。
 *   难度：入门，通过率52.45%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 正确计算三科平均分（浮点运算，避免整数除法截断）；
 *   2. 判断平均分是否<60，按规则输出YES/NO（大写）。
 * 解题思路：
 *   1. 用double接收成绩（或int接收后转浮点计算，避免整数除法误差）；
 *   2. 计算平均分s=(a+b+c)/3；
 *   3. 条件判断：s<60输出YES，否则输出NO。
 */
#include <bits/stdc++.h>
using namespace std;
int main (){
    double a,b,c;
    cin>>a>>b>>c;
    double s=0;
    s = (a+b+c)/3;  // 浮点运算计算平均分，避免整数除法截断（如180/3=60，179/3≈59.67）
    if(s<60){       // 平均分<60，被叫家长
        cout<<"YES"<<endl;
    }
    else{           // 平均分≥60，不被叫家长
        cout<<"NO"<<endl;
    }
    return 0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 浮点运算：必须用浮点类型计算平均分（若用int接收成绩，需写成(double)(a+b+c)/3，避免179/3=59（整数除法截断））；
2. 输出格式：示例验证输出为大写YES/NO（题目描述写Y/N，以示例为准）；
3. 条件判断：严格判断s<60（≥60均输出NO，包含刚好60的情况）；
4. 数据范围：成绩0~100，总分0~300，平均分0~100，double/int均可覆盖。

【易错点】
1. 整数除法陷阱：
   - 用int接收成绩且直接计算(a+b+c)/3（如179/3=59，180/3=60，结果正确；但179.9/3≈59.97会被截断，本题成绩为整数，可但浮点更稳妥）；
2. 输出错误：
   - 输出小写yes/no（示例要求大写YES/NO）；
   - 输出Y/N（题目描述笔误，示例验证为YES/NO）；
3. 条件错误：把s<60写成s<=60（60分及格却输出YES，结果错误）；
4. 语法错误：平均分计算漏括号（如a+b+c/3，运算优先级错误，变成a+b+(c/3)）。
*/