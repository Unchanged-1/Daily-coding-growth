/*
 * 题目：多组数据a+b III
 * 来源：牛客网（noob30）
 * 知识类型：C++基础语法 | 循环结构（while无限循环+break终止） | 多组输入（无组数的终止型输入） | 算术运算（加法） | 条件判断（终止条件）
 * 题目原文：
 *   描述：计算多组a+b的和，当输入a=0且b=0时结束（该组不处理）。
 *   输入描述：多行输入两个整数a,b（0≦a,b≦1000），空格隔开；a=0且b=0时输入结束。
 *   输出描述：每组数据输出一行a+b的结果（0 0组不输出）。
 *   示例1：输入1 1、2 2、0 0 → 输出2、4。
 *   难度：入门，通过率61.97%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 处理无组数的多组输入，直到遇到0 0终止；
 *   2. 0 0组不输出结果，其他组输出求和结果。
 * 解题思路：
 *   1. 用while(1)创建无限循环；
 *   2. 每次循环读取a和b；
 *   3. 判断a==0且b==0时break终止循环；
 *   4. 否则输出a+b的结果。
 */
#include<bits/stdc++.h>
using namespace std;
int main() {
    int a,b;
    while(1){  // 无限循环，直到遇到break终止
        cin>>a>>b;  // 读取每组的两个整数
        if(a==0&&b==0){  // 终止条件：a和b均为0，跳出循环
            break;
        }
        cout<<a+b<<endl;  // 非终止组，输出求和结果
    }
    return 0;
}

