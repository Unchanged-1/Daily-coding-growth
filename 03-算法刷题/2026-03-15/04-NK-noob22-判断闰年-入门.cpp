/*
 * 题目：判断闰年
 * 来源：牛客网（noob22）
 * 题目原文（修正笔误后）：
 *   描述：给定整数n（1≦n≦2018），判断是否为闰年。
 *   正确闰年规则：
 *     1. 能被400整除 → 闰年；
 *     2. 能被4整除但不能被100整除 → 闰年；
 *     3. 其他情况 → 非闰年。
 *   输入描述：一行输入整数n（1≦n≦2018）。
 *   输出描述：闰年输出yes，否则输出no。
 *   示例1：输入2000 → 输出yes；示例2：输入1900 → 输出no。
 *   难度：入门，通过率46.93%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 严格按闰年规则判断（需同时考虑400/100/4的整除关系）；
 *   2. 输出固定字符串yes/no（小写）。
 * 解题思路：
 *   1. 用int接收n（1~2018范围）；
 *   2. 条件判断：(n%400==0) || (n%4==0 && n%100!=0) → 闰年；
 *   3. 按条件输出yes/no。
 */
#include <bits/stdc++.h>
using namespace std;
int main(){
    int n;
    cin>>n;
    // 核心判断逻辑：能被400整除，或能被4整除但不能被100整除 → 闰年
    if((n%400==0)||(n%4==0&&n%100!=0)){
        cout<<"yes"<<endl;
    }
    else{
        cout<<"no"<<endl;
    }
    return 0;
}

