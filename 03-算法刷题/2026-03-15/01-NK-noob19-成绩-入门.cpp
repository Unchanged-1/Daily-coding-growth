/*
 * 题目：成绩
 * 来源：牛客网（noob19）
 * 题目原文：
 *   描述：总成绩S = A×20% + B×30% + C×50%；A/B/C为10的倍数，输出整数S。
 *   输入描述：一行输入三个非负整数A,B,C（0≦A,B,C≦100），均为10的倍数。
 *   输出描述：输出整数S（总成绩）。
 *   示例1：输入100 100 100 → 输出100。
 *   难度：入门，通过率74.57%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 按权重公式计算总成绩（20%/30%/50%分别对应作业/小测/期末）；
 *   2. 输出整数结果（题目保证结果为整数）。
 * 解题思路：
 *   1. 用int接收A,B,C（0~100范围）；
 *   2. 代入公式计算S（浮点运算后自动转int，或用整数运算(a*2 + b*3 + c*5)/10）；
 *   3. 输出整数S。
 */
#include<bits/stdc++.h>
using namespace std;
int main(){
    int a,b,c;
    cin>>a>>b>>c;
    int s;
    s = a*0.2 + b*0.3 + c*0.5;  // 浮点运算：利用题目保证结果为整数的条件，直接转int
    cout<<s<<endl;    
    return 0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 权重计算：严格按20%/30%/50%（即0.2/0.3/0.5）计算，权重和为1；
2. 结果特性：题目保证A/B/C为10的倍数，因此s必为整数，无需额外取整；
3. 可选优化（纯整数运算，避免浮点误差）：
   s = (a*2 + b*3 + c*5) / 10;  // 等价于0.2a+0.3b+0.5c，无浮点运算更稳妥。

【易错点】
1. 权重错误：误用比例（如0.3/0.2/0.5，颠倒作业和小测权重）；
2. 浮点精度陷阱：若A/B/C非10的倍数，直接转int可能误差，但本题无需担心；
3. 输入错误：分行读取A/B/C（题目要求一行输入）；
4. 输出格式：额外添加空格/换行（仅需输出整数S）。
*/