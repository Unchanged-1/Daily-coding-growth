/*
 * 题目：卡拉兹函数
 * 来源：牛客网（noob24）
 * 知识类型：C++基础语法 | 条件判断（if-else） | 算术运算（奇偶判断/乘除） | 函数逻辑实现
 * 题目原文：
 *   描述：卡拉兹函数定义：n为奇数则f(n)=3n+1；n为偶数则f(n)=n/2。给定正整数n，计算并输出f(n)。
 *   输入描述：一行输入正整数n（1≦n≦10⁵）。
 *   输出描述：输出f(n)的整数值。
 *   示例1：输入1 → 输出4；示例2：输入2 → 输出1。
 *   难度：入门，通过率69.77%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 正确判断n的奇偶性（用取余运算n%2）；
 *   2. 按卡拉兹规则计算并输出结果。
 * 解题思路：
 *   1. 用int接收n（10⁵范围，int足够）；
 *   2. 奇偶判断：n%2==0为偶数，否则为奇数；
 *   3. 偶数输出n/2，奇数输出3*n+1。
 */
#include <bits/stdc++.h>
using namespace std;
int main(){
    int n;
    cin>>n;
    if(n%2==0){          // 偶数：执行n/2
        cout<<n/2<<endl;
    }
    else {               // 奇数：执行3*n+1
        cout<<3*n+1<<endl;
    }
    return 0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 奇偶判断：用n%2==0判断偶数（取余运算为0则是偶数，非0则是奇数）；
2. 运算规则：严格按卡拉兹函数定义（奇数3n+1，偶数n/2，顺序不可颠倒）；
3. 数据范围：n≤10⁵时，3*n+1≤3.00001×10⁵ < int最大值，无需long long。

【易错点】
1. 奇偶判断错误：把n%2==0写成n%2==1（判断奇数时写反，结果完全错误）；
2. 运算错误：
   - 奇数写成3*(n+1)（括号错误，变成3n+3）；
   - 偶数写成n/1（笔误，把2写成1）；
3. 输入错误：接收n时用double类型（后续运算会有浮点误差，题目要求整数）；
4. 输出错误：额外添加空格/换行（如cout<<" "<<n/2<<endl）。
*/