/*
 * 题目：明天星期几？
 * 来源：牛客网（noob21）
 * 题目原文：
 *   描述：用1~7表示周一到周日，已知今天是星期d（1≦d≦7），推算明天是星期几。
 *   输入描述：一行输入整数d（1≦d≦7）。
 *   输出描述：输出明天的星期数（1~7）。
 *   示例1：输入1 → 输出2；输入7 → 输出1。
 *   难度：入门，通过率54.27%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 处理边界：d=7时，明天是1；其他情况d+1；
 *   2. 输出1~7范围内的整数。
 * 解题思路：
 *   1. 用int接收d（1~7范围）；
 *   2. 条件判断：d=7则输出1，否则输出d+1；
 *   （可选优化：用取余运算 d%7 + 1，更简洁）。
 */
#include <bits/stdc++.h>
using namespace std;
int main(){
    int d;
    cin>>d;
    if(d==7){          // 边界处理：周日（7）的明天是周一（1）
        cout<<1<<endl;
    }
    else {             // 其他情况：明天=今天+1
        cout<<d+1<<endl;
    }
    return 0;
}

/* 可选优化写法（取余法，更简洁）：
#include <bits/stdc++.h>
using namespace std;
int main(){
    int d;
    cin>>d;
    cout << (d % 7) + 1 << endl;  // 1~6: d%7=d → d+1；7%7=0 → 0+1=1
    return 0;
}
*/

