/*
 * 题目：牛牛学数列
 * 来源：牛客网（noob32）
 * 知识类型：C++基础语法 | 循环结构（for循环） | 数列求和（符号交替） | 条件判断（奇偶） | 算术运算（加减） | 数学公式优化
 * 题目原文：
 *   描述：计算数列前n项和 S(n) = 1 - 2 + 3 - 4 + … + (-1)^(n-1)×n（1≦n≦100）。
 *   输入描述：一行输入正整数n（1≦n≦100）。
 *   输出描述：输出S(n)的整数值。
 *   示例1：输入4 → 输出-2（1-2+3-4=-2）。
 *   难度：入门，通过率55.99%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 按符号交替规则计算数列和（奇数项加，偶数项减）；
 *   2. 输出最终求和结果。
 * 解题思路（循环法，新手友好）：
 *   1. 初始化求和变量sum=0；
 *   2. 循环i从1到n：奇数项sum+=i，偶数项sum-=i；
 *   3. 输出sum。
 */
#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n, sum = 0;
    cin >> n;
    // 循环遍历1~n，按奇偶交替加减
    for (int i = 1; i <= n; i++)
    {
        if (i % 2 != 0)
        { // 奇数项：加当前数（对应(-1)^(i-1)=1）
            sum += i;
        }
        else
        { // 偶数项：减当前数（对应(-1)^(i-1)=-1）
            sum -= i;
        }
    }
    cout << sum << endl; // 输出最终和
    return 0;
}

