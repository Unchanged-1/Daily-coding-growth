/*
======= 核心重点 & 易错点 =======
【重点】
1. 符号规则（核心逻辑）：
   - 奇数项（i=1、3、5…）：加i，对应公式中(-1)^(i-1)=1；
   - 偶数项（i=2、4、6…）：减i，对应公式中(-1)^(i-1)=-1；
2. 循环法优势：逻辑直观，新手易理解，n≤100时循环次数少，无性能问题；
3. 数学公式优化：可规避循环，直接按奇偶性计算（偶数和=-n/2，奇数和=(n+1)/2）；
4. 数据范围：n≤100，sum的最大值为50（n=99）、最小值为-50（n=100），int完全覆盖。

【易错点】
1. 符号颠倒：把奇数项减、偶数项加（如i%2!=0时sum-=i，结果完全错误）；
2. 循环范围错误：i从0开始（多加0，不影响结果但逻辑冗余）或i<n（漏算第n项）；
3. 变量初始化错误：sum未初始化（取随机值，结果错误）；
4. 公式法易错点：奇数项写成n/2（如n=3时n/2=1，正确应为(3+1)/2=2）。
*/