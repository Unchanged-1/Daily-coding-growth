/*
 * 题目：左侧严格小于计数
 * 来源：牛客网（noob42）
 * 知识类型：C++基础语法 | 双重循环（枚举） | 计数优化（利用数值范围） | 输出格式控制
 * 题目原文：
 *   描述：给定长度n的整数序列a，计算结果序列b：b[i]是a[1~i-1]中严格小于a[i]的数的个数（1≤i≤n）。
 *   输入描述：
 *     第一行输入n（1≤n≤100）；
 *     第二行输入n个整数a[1~n]（0≤a[i]≤10），空格分隔。
 *   输出描述：一行空格分隔输出b[1~n]，无末尾空格。
 *   示例1：输入5、1 2 3 4 5 → 输出0 1 2 3 4；
 *   示例2：输入6、4 4 2 1 3 5 → 输出0 0 0 0 2 5。
 *   难度：简单，通过率50.91%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 正确统计每个位置左侧严格小于当前数的元素个数；
 *   2. 输出格式正确（无末尾空格）。
 * 解题思路（双重循环法，你的核心逻辑）：
 *   1. 遍历每个元素a[i]（i从1到n）；
 *   2. 内层循环遍历i左侧所有元素a[j]（j从1到i-1），统计严格小于a[i]的个数；
 *   3. 实时计算并输出b[i]，控制空格格式（最后一个元素无空格）。
 */
#include <bits/stdc++.h>
using namespace std;

int a[102], b[102]; // 数组大小102，适配n≤100的要求
int main() {
    int n;
    cin >> n;
    // 遍历每个元素，计算b[i]并输出
    for(int i = 1; i <= n; i++){
        cin >> a[i]; // 读取当前元素
        int cnt = 0; // 统计左侧严格小于a[i]的个数
        // 内层循环：遍历i左侧所有元素
        for(int j = 1; j <= i-1; j++){
            if(a[j] < a[i]){ // 严格小于，核心判断条件
                cnt++;
            }
        }
        b[i] = cnt; // 存储结果（可选，也可直接输出）
        // 格式控制：最后一个元素输出后无空格/换行，其余加空格
        if(i == n){
            cout << b[i] << endl;
        } else {
            cout << b[i] << ' ';
        }
    }
    return 0; // 补充return，代码更规范
}

