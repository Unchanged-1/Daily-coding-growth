/*
 * 题目：神秘石像的镜像序列
 * 来源：牛客网（noob41）
 * 知识类型：C++基础语法 | 数组/动态数组（vector） | 循环读取输入 | 倒序输出 | 空格格式控制
 * 题目原文：
 *   描述：读取以0结尾的整数序列，倒序输出除0外的所有数（1≤n≤100，数的范围1≤a_i≤2³¹-1）。
 *   输入描述：一行输入若干非负整数，最后一个为0，其余为正整数。
 *   输出描述：一行空格分隔输出倒序序列（无末尾空格）。
 *   示例1：输入8 15 3 42 7 0 → 输出7 42 3 15 8；示例2：输入10 20 30 40 50 0 → 输出50 40 30 20 10。
 *   难度：简单，通过率49.50%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 正确读取以0结尾的序列（终止条件）；
 *   2. 倒序输出且格式正确（无末尾空格）；
 *   3. 适配数据范围（支持大数存储）。
 * 解题思路（数组法，你的核心逻辑）：
 *   1. 定义数组存储输入数，循环读取直到遇到0；
 *   2. 统计有效数的个数（count）；
 *   3. 倒序遍历数组，控制空格格式（第一个数直接输出，后续数前加空格）。
 */
#include <bits/stdc++.h>
using namespace std;

int main()
{
    int a[101];       // 数组大小101，适配n≤100的要求
    int n, count = 0; // count记录有效数的个数
    // 循环读取输入，遇到0终止
    while (cin >> n)
    {
        if (n == 0)
        {
            break; // 终止条件：读取到0，停止输入
        }
        a[count] = n; // 存储有效数到数组
        count++;
    }
    // 倒序输出，控制空格格式（无末尾空格）
    for (int i = count - 1; i >= 0; i--)
    {
        if (i == count - 1)
        { // 第一个输出的数（原最后一个数），无前置空格
            cout << a[i];
        }
        else
        { // 其余数前加空格
            cout << ' ' << a[i];
        }
    }
    return 0;
}

