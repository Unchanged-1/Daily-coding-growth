/*
 * 题目：牛牛数数
 * 来源：牛客网（noob38）
 * 知识类型：C++基础语法 | 枚举（遍历） | 数位操作（逐位判断） | 条件筛选 | 效率优化
 * 题目原文：
 *   描述：列出1~n中既不包含数字4、又不是4的倍数的整数，按升序输出（1≦n≦10⁵）。
 *   输入描述：一行输入正整数n（1≦n≦10⁵）。
 *   输出描述：符合条件的数字按升序每行输出一个。
 *   示例1：输入9 → 输出1、2、3、5、6、7、9（跳过4、8）。
 *   难度：简单，通过率29.94%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 双重筛选：排除4的倍数 + 排除含数字4的数；
 *   2. 按升序输出符合条件的数。
 * 解题思路（枚举+数位判断，你的核心逻辑）：
 *   1. 遍历1~n的所有整数；
 *   2. 第一步筛选：是4的倍数则跳过；
 *   3. 第二步筛选：逐位判断是否含数字4，含则跳过；
 *   4. 剩余数字按序输出。
 */
#include<bits/stdc++.h>
using namespace std;

int main() {
    int n;
    cin>>n;
    // 枚举1~n的所有数，逐一筛选
    for(int i=1;i<=n;i++){
        // 第一步：排除4的倍数
        if(i%4 == 0){
            continue;
        }
        
        // 第二步：逐位判断是否包含数字4
        int temp = i; // 临时变量存储当前数，避免修改i
        bool is_valid = true; // 标记是否符合条件（初始为符合）
        while(temp > 0){
            int digit = temp % 10; // 提取最后一位
            if(digit == 4){ // 含数字4，标记为不符合
                is_valid = false;
                break; // 提前终止数位判断，优化效率
            }
            temp /= 10; // 去掉最后一位
        }
        
        // 符合条件则输出
        if(is_valid){
            cout << i << endl;
        }
    }
    return 0;
}

