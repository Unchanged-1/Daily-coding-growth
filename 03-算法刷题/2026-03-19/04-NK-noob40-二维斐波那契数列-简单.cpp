/*
 * 题目：二维斐波那契数列
 * 来源：牛客网（noob40）
 * 知识类型：C++基础语法 | 二维动态规划 | 递推数列 | 取模运算 | 空间优化（二维→一维）
 * 题目原文：
 *   描述：二维斐波那契数列递推规则：
 *         1. a[1][1] = 1；
 *         2. j=1且i≥2时，a[i][j] = a[i-1][j]；
 *         3. i=1且j≥2时，a[i][j] = a[i][j-1]；
 *         4. i≥2且j≥2时，a[i][j] = a[i-1][j] + a[i][j-1]；
 *         求a[n][m] mod 1e9+7的值（1≦n,m≦1000）。
 *   输入描述：一行输入两个正整数n、m（行/列下标）。
 *   输出描述：输出取模后的结果。
 *   示例1：输入1 1 → 输出1；示例2：输入2 2 → 输出2。
 *   难度：简单，通过率25.51%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 正确实现二维递推公式；
 *   2. 全程取模避免数值溢出；
 *   3. 适配n,m≤1000的范围。
 * 解题思路（二维数组法，你的核心逻辑）：
 *   1. 定义二维数组存储递推值（大小1003×1003，避免下标越界）；
 *   2. 初始化a[1][1]=1；
 *   3. 按行/列遍历，分情况执行递推公式，每步取模；
 *   4. 输出a[n][m]。
 */
#include<bits/stdc++.h>
using namespace std;

const int MOD = 1000000007; // 常量定义放在顶部，更规范
int a[1003][1003]; // 1003×1003适配n,m≤1000（下标1~1000）

int main() {
    int n, m;
    cin >> n >> m;
    
    // 双重循环遍历所有位置，按递推规则赋值
    for(int i = 1; i <= n; i++){
        for(int j = 1; j <= m; j++){
            if(i == 1 && j == 1){ // 初始条件：a[1][1]=1
                a[i][j] = 1 % MOD;
            }
            else if(i == 1 && j >= 2){ // 第一行（除1,1）：等于左边值
                a[i][j] = a[i][j-1] % MOD;
            }
            else if(j == 1 && i >= 2){ // 第一列（除1,1）：等于上边值
                a[i][j] = a[i-1][j] % MOD;
            }
            else { // 其他位置：上边+左边，取模避免溢出
                a[i][j] = (a[i][j-1] + a[i-1][j]) % MOD;
            }
        }
    }
    cout << a[n][m] << endl;
    return 0;
}

