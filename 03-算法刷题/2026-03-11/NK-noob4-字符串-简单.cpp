/*
 * 题目：牛牛学说话之-字符串
 * 来源：牛客网（noob4）
 * 题目原文：
 *   描述：会说浮点数之后，牛牛开始尝试字符串。输入一个仅由大写字母、小写字母和数字构成的字符串s，长度n（1≦n≦10⁴），输出该字符串。
 *   输入描述：输入一行一个长度为n的字符串s（仅含大小写字母、数字，1≦n≦10⁴）。
 *   输出描述：输出字符串s。
 *   难度：入门，通过率55.36%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 读取长度1~10⁴的字母/数字混合字符串s；
 *   2. 直接输出该字符串，无格式额外要求。
 * 解题思路：
 *   1. 用C++ string类型接收输入的字符串s；
 *   2. 直接用cout输出s即可。
 */
#include <bits/stdc++.h>
using namespace std;
int main () {
    string s;
    cin >> s;
    cout << s;
    return  0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 用string类型存储字符串（适配1~10⁴的长度，无需手动管理数组）；
2. cin/cout可直接处理string类型的输入输出，简洁高效。

【易错点】
1. 误用char数组存储（需手动指定长度，易因数组大小不足导致越界）；
2. 输入时误用getline（cin>>s已能读取无空格的整行字符串，getline会读取空行/换行符导致错误）；
3. 输出时额外添加空格/换行（题目仅要求输出字符串本身）。
*/