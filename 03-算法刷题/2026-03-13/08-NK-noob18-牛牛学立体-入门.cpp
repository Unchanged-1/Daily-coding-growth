/*
 * 题目：牛牛学立体
 * 来源：牛客网（noob18）
 * 题目原文：
 *   描述：给定长方体的长a、宽b、高c（1≦a,b,c≦10³），计算表面积和体积。
 *   公式：
 *     表面积S = 2(ab + bc + ca)；
 *     体积V = abc。
 *   输入描述：一行输入三个整数a,b,c（1≦a,b,c≦10³）。
 *   输出描述：第一行输出表面积S，第二行输出体积V。
 *   示例1：输入1 1 1 → 输出6（表面积）、1（体积）。
 *   难度：入门，通过率69.56%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 按公式正确计算长方体的表面积和体积；
 *   2. 输出格式：表面积占一行，体积占一行。
 * 解题思路：
 *   1. 用int接收a、b、c（10³范围，乘积最大10⁹，int可覆盖）；
 *   2. 代入公式计算表面积S和体积V；
 *   3. 按要求分行输出S和V。
 */
#include <bits/stdc++.h>
using namespace std;

int main()
{
    int a, b, c;
    cin >> a >> b >> c;
    int S = 2 * (a * b + b * c + c * a); // 表面积公式：2*(长×宽 + 宽×高 + 高×长)
    int V = a * b * c;                   // 体积公式：长×宽×高
    cout << S << endl
         << V << endl; // 分行输出表面积、体积
    return 0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 公式正确性：
   - 表面积：必须乘2，且括号内是ab+bc+ca（漏乘2是高频错误）；
   - 体积：直接三者相乘，无额外系数；
2. 数据范围：a,b,c≤10³，体积最大10⁹ < int最大值（2³¹-1≈2e9），int足够；
3. 输出格式：表面积和体积需分行输出（用endl分隔）。

【易错点】
1. 公式错误：
   - 表面积漏乘2（如S=ab+bc+ca，结果缩小一半）；
   - 表面积括号错误（如2*ab + bc + ca，运算优先级错误）；
2. 输出格式错误：将S和V输出在同一行（如cout<<S<<' '<<V）；
3. 类型误用：不必要使用long long（10³×10³×10³=10⁹，int可覆盖）；
4. 输入错误：试图分行读取a、b、c（题目要求一行输入）。
*/