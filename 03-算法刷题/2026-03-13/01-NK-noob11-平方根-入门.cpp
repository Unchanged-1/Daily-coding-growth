/*
 * 题目：平方根
 * 来源：牛客网（noob11）
 * 题目原文：
 *   描述：给定一个正整数n（1≦n≦10⁹），求n的平方根的整数部分（向下取整）。
 *   输入描述：在一行中输入一个整数n（1≦n≦10⁹）。
 *   输出描述：输出n的平方根向下取整后的值。
 *   示例1：输入5 → 输出2；示例2：输入16 → 输出4。
 *   难度：入门，通过率63.44%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 计算1~10⁹范围内整数n的平方根；
 *   2. 对平方根结果向下取整，输出整数部分。
 * 解题思路：
 *   1. 用double接收n（适配sqrt函数的参数类型）；
 *   2. 调用sqrt()函数计算平方根；
 *   3. 强制类型转换为int（自动向下取整），输出结果。
 */
#include <bits/stdc++.h>
using namespace std;

int main()
{
    double a;
    cin >> a;
    cout << (int)sqrt(a) << endl;
    return 0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 函数使用：sqrt()函数需包含<cmath>（万能头<bits/stdc++.h>已包含）；
2. 类型转换：sqrt返回double，强制转int自动向下取整（核心逻辑）；
3. 数据范围：double精度足够覆盖10⁹的平方根计算（最大3e4+）。

【易错点】
1. 类型错误：用int接收n后直接传sqrt（sqrt参数需浮点型，int会隐式转换但易踩坑）；
2. 取整错误：试图用floor()函数（多余，int强制转换已实现向下取整）；
3. 头文件缺失：不用万能头时漏加<cmath>（导致sqrt函数未定义）；
4. 范围误判：担心10⁹超出double范围（double可精确表示10⁹以内整数）。
*/