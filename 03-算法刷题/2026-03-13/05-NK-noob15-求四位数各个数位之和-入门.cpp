/*
 * 题目：求四位数各个数位之和
 * 来源：牛客网（noob15）
 * 题目原文：
 *   描述：给定四位整数n（1000≦n≦9999），计算该整数各个数位之和。
 *   输入描述：在一行中输入一个四位整数n（1000≦n≦9999）。
 *   输出描述：输出一个整数，表示n的各个位数之和。
 *   示例1：输入1270 → 输出10；示例2：输入9999 → 输出36。
 *   难度：入门，通过率70.63%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 提取四位整数的每一位数字；
 *   2. 计算所有数位的和并输出。
 * 解题思路（字符串法，简洁高效）：
 *   1. 用string接收四位数n，逐字符遍历每一位；
 *   2. 将字符型数字转换为整型（n[i]-'0'），累加求和；
 *   3. 输出求和结果。
 */
#include <bits/stdc++.h>
using namespace std;
int main()
{
    string n;
    int sum = 0;
    cin >> n;
    for (int i = 0; i < n.size(); i++)
    {
        sum += n[i] - '0'; // 字符转数字：ASCII码差值，如'1'-'0'=1
    }
    cout << sum << endl;
    return 0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 字符转数字：必须用n[i]-'0'（直接用n[i]会取ASCII码值，如'1'=49）；
2. 遍历范围：n.size()=4（四位整数），遍历0~3即可覆盖所有数位；
3. 求和初始化：sum初始值必须为0（否则会累加随机值）。

【易错点】
1. 类型转换错误：直接sum +=n[i]（累加ASCII码，结果错误）；
2. 求和初始化错误：sum未赋值（默认随机值，结果混乱）；
3. 可选补充（数学法）：
   int n, sum=0;
   cin>>n;
   while(n>0){
       sum +=n%10;  // 取个位
       n /=10;      // 去掉个位
   }
   （两种方法均可，字符串法更简洁）。
*/