/*
 * 题目：反向输出一个四位数
 * 来源：牛客网（noob12）
 * 题目原文：
 *   描述：给定四位整数n（1000≦n≦9999），反向输出各位数字；反向后高位为零需保留（如1000反向输出0001）。
 *   输入描述：在一行中输入一个四位整数n（1000≦n≦9999）。
 *   输出描述：输出四位整数，为n反向后的结果，保留前导零。
 *   示例1：输入1234 → 输出4321；示例2：输入1000 → 输出0001。
 *   难度：入门，通过率46.96%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 反向输出四位整数的每一位数字；
 *   2. 必须保留前导零（输出为4位，如1000反向需输出0001而非1）。
 * 解题思路（三种写法）：
 *   写法1（手动遍历字符串）：从后往前遍历字符串，逐字符输出；
 *   写法2（reverse函数法，最简洁）：调用C++ STL的reverse函数直接反转字符串；
 *   写法3（数学法）：通过取余/整除拆分每一位，固定循环4次输出。
 */

