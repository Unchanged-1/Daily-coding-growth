// 写法2：reverse函数法（C++ STL，最简洁推荐）
#include <bits/stdc++.h>
using namespace std;

int main()
{
    string n;
    cin >> n;
    reverse(n.begin(), n.end()); // 直接反转字符串（需包含<algorithm>，万能头已包含）
    cout << n << endl;
    return 0;
}

// 写法3：数学法（纯数值运算，无字符串）
