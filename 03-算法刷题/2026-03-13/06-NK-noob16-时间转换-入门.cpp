/*
 * 题目：时间转换
 * 来源：牛客网（noob16）
 * 题目原文：
 *   描述：给定秒数seconds（0≦seconds≦10⁸），转换为小时h、分钟m、秒s（m和s≤59），总时间不变。
 *   输入描述：一行输入整数seconds（0≦seconds≦10⁸）。
 *   输出描述：一行输出h、m、s三个整数，单个空格分隔（可为0）。
 *   示例1：输入3661 → 输出1 1 1；示例2：输入67 → 输出0 1 7。
 *   难度：入门，通过率50.40%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 按「小时=总秒数/3600，剩余秒数=总秒数%3600；分钟=剩余秒数/60，秒=剩余秒数%60」转换；
 *   2. 输出格式为「h m s」（单个空格分隔）。
 * 解题思路：
 *   1. 用int接收总秒数n（10⁸≤2.78×10⁴小时，int可覆盖）；
 *   2. 分步计算：h = n/3600 → 剩余秒数n = n%3600 → m = n/60 → s = n%60；
 *   3. 按格式输出h、m、s。
 */
#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cin >> n;
    int h = n / 3600; // 计算小时数：1小时=3600秒
    n %= 3600;        // 剩余秒数（去掉小时部分）
    int m = n / 60;   // 计算分钟数：1分钟=60秒
    int s = n % 60;   // 剩余秒数（去掉分钟部分）
    cout << h << ' ' << m << ' ' << s << endl;
    return 0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 转换逻辑：先算小时（/3600），再用取余保留剩余秒数，再算分钟/秒（/60、%60）；
2. 数据范围：int可覆盖10⁸秒（int最大值约2×10⁹），无需long long；
3. 输出格式：单个空格分隔，不能多空格/无空格。

【易错点】
1. 计算顺序错误：先算分钟再算小时（如先n/60，导致小时数计算错误）；
2. 取余遗漏：计算h后未执行n%=3600（直接用原n算m，结果错误）；
3. 输出格式错误：用endl/多个空格分隔（如h<<endl<<m，或h<<"  "<<m）；
4. 类型误用：不必要使用long long（徒增代码复杂度）。
*/