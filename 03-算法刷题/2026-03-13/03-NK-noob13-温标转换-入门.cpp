/*
 * 题目：温标转换
 * 来源：牛客网（noob13）
 * 题目原文：
 *   描述：已知开尔文温度K，按公式转换为华氏温度F：C=K-273.15，F=C×1.8+32。
 *   输入描述：在一行中输入一个实数K（0≦K≦10⁵），表示开尔文温度。
 *   输出描述：输出实数F（华氏温度），误差≤10⁻³即被接受。
 *   示例1：输入173.56 → 输出-147.261999999；示例2：输入273.15 → 输出32.00000000000。
 *   难度：入门，通过率72.88%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 按公式完成K→C→F的温标转换；
 *   2. 输出结果误差≤10⁻³即可，保留足够小数位数（如9位）更稳妥。
 * 解题思路：
 *   1. 用double接收K（适配实数范围，精度满足误差要求）；
 *   2. 代入公式计算F：F = (K-273.15)*1.8 + 32；
 *   3. 用fixed+setprecision(9)输出（保证精度，满足误差要求）。
 */
#include <bits/stdc++.h>
using namespace std;
int main()
{
    double K, F;
    cin >> K;
    F = (K - 273.15) * 1.8 + 32;
    cout << fixed << setprecision(9) << F << endl;
    return 0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 公式正确性：先算K-273.15得到摄氏温度，再代入F的公式（不可省略括号）；
2. 数据类型：必须用double（float精度不足，易触发误差超限）；
3. 精度控制：fixed+setprecision(9)输出，既满足误差要求，又贴合示例格式。

【易错点】
1. 公式错误：漏加括号写成 K-273.15*1.8+32（运算优先级错误，先乘后减）；
2. 类型误用：用float存储K/F（float精度约6~7位有效数字，可能误差超限）；
3. 精度不足：仅保留1~2位小数（如输出-147.26，虽满足误差，但易踩格式坑）；
4. 头文件缺失：不用万能头时漏加<iomanip>（导致setprecision未定义）。
*/