/*
 * 题目：计算机内存
 * 来源：牛客网（noob17）
 * 题目原文：
 *   描述：已知1MB=1024KB、1KB=1024B、1B=1字节，一个整数占4字节；给定n MB内存（1≦n≦256），计算可存储的整数个数。
 *   输入描述：一行输入整数n（1≦n≦256），表示内存大小（单位MB）。
 *   输出描述：输出可存储的整数个数（整数）。
 *   示例1：输入1 → 输出262144；示例2：输入256 → 输出67108864。
 *   难度：入门，通过率76.26%，时间限制1秒，空间限制256M。
 * 核心要求：
 *   1. 按单位换算公式计算：总字节数 = n × 1024 × 1024；
 *   2. 整数个数 = 总字节数 ÷ 4（每个整数占4字节）。
 * 解题思路：
 *   1. 用int接收n（1≦n≦256，范围小）；
 *   2. 按公式计算：n × 1024 × 1024 ÷ 4；
 *   3. 直接输出计算结果。
 */
#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cin >> n;
    // 计算逻辑：n MB → 字节数（n*1024*1024）→ 除以4得到整数个数
    cout << n * 1024 * 1024 / 4 << endl;
    return 0;
}

/*
======= 核心重点 & 易错点 =======
【重点】
1. 单位换算：严格按1MB=1024KB、1KB=1024B计算（不可用1000替代1024）；
2. 计算顺序：先算总字节数（n*1024*1024），再除以4（整除无余数）；
3. 数据范围：n=256时，256*1024*1024/4=67108864 < int最大值（2^31-1≈2e9），int足够。

【易错点】
1. 换算错误：用1000代替1024（如n*1000*1000/4，结果偏小）；
2. 计算顺序错误：先除以4再乘1024（结果正确但逻辑不直观，建议先算总字节数）；
3. 类型误用：不必要使用long long（n=256时结果仍在int范围内）；
4. 公式遗漏：漏乘1024（如n*1024/4，少算一级换算）。
*/